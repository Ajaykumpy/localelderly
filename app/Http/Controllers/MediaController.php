     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->hasFile('file')){
            $upload=new \App\Helpers\UploadHandler(['param_name'=>'file','upload_dir'=>'public/uploads/','upload_url'=>asset('public/uploads/').'/','image_versions'=>[],'print_response'=>false,'accept_file_types' => '/\.(gif|jpe?g|png|webp)$/i',]);
            if($upload->get_response() && count($upload->get_response()['file'])>0){         
                                  
                return response()->json($upload->get_response()['file']);
            }
        }
    }

