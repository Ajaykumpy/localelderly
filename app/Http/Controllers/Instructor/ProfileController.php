    public function edit()
    {
        $doctor = Doctor::with('profile')->find(auth()->user()->id);
        return view('doctor.profile-edit',compact('doctor'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $doctor = Doctor::find(auth()->user()->id);
        $doctor->name = $request->name??'';
        $doctor->email = $request->email;
        if($request->has('password') && !empty($request->password)){
            $doctor->password = bcrypt($request->password);
        }
        $doctor->save();
        if(!$doctor){
            return redirect()->back()->withErrors(['Something went wrongs']);
        }
        return redirect()->route('doctor.dashboard')->with('success', 'Saved Successfully!');
    }

