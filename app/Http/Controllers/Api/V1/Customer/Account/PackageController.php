		$package=PackageSubscription::where('subscriber_type',get_class(auth()->user()))->where('subscriber_id',auth()->id())->whereNotNull('ends_at')->get();
        if($package->count()>0){
            $package=$package->filter(function($items){
                return $items->active();
            });
        }
        return response()->json([
            'success'=>true,
            'status'=>$package->count()>0?1:0
        ]);
    }

